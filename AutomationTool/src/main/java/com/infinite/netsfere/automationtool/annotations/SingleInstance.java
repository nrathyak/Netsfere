package com.infinite.netsfere.automationtool.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is used when we want to create (only one) object of a
 * particular field(modifier should be <b>public</b>) in a class. This can only
 * be used for class fields. <b>PageFactory</b> will be used to instantiate the
 * object. <br>
 * NOTE: No need to initialize this field in your program.
 * 
 * @see com.infinite.netsfere.automationtool.modules.PageFactory
 */

@Target(ElementType.FIELD)
@Retention(RetentionPolicy.RUNTIME)
public @interface SingleInstance {

}
